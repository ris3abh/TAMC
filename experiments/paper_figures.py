def main() -> None:
    import argparse

    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument(
        "--regenerate-missing",
        action="store_true",
        help="Also regenerate moderately expensive (multi-minute) missing summaries "
        "(forecast-adaptation multiseed CSVs). The topology ablation summary is never "
        "auto-regenerated regardless of this flag.",
    )
    parser.add_argument(
        "--skip-expensive",
        action="store_true",
        help="Skip even the lightweight default regeneration (detection summaries, "
        "runtime benchmark); only use whatever is already on disk.",
    )
    args = parser.parse_args()

    regenerate_lightweight = not args.skip_expensive
    regenerate_expensive = args.regenerate_missing and not args.skip_expensive

    FIGURES_DIR.mkdir(parents=True, exist_ok=True)

    figure_specs = [
        ("paper_pipeline_schematic.png", lambda p: make_pipeline_schematic(p)),
        (
            "paper_detection_summary.png",
            lambda p: make_detection_summary(p, regenerate_lightweight),
        ),
        (
            "paper_adaptation_tradeoff.png",
            lambda p: make_adaptation_tradeoff(p, regenerate_expensive),
        ),
        ("paper_ablation_heatmap.png", lambda p: make_ablation_heatmap(p)),
        (
            "paper_runtime_summary.png",
            lambda p: make_runtime_summary(p, regenerate_lightweight),
        ),
        (
            "paper_dynamical_adaptation_summary.png",
            lambda p: make_dynamical_adaptation_summary(p, regenerate_expensive),
        ),
        (
            "paper_regime_benchmark_summary.png",
            lambda p: make_regime_benchmark_summary(p, regenerate_expensive),
        ),
    ]

    created: list[str] = []
    skipped: list[str] = []

    for filename, builder in figure_specs:
        output_path = FIGURES_DIR / filename
        print(f"\n[{filename}]")
        try:
            success = builder(output_path)
        except Exception as exc:  # noqa: BLE001 - keep generating the rest
            print(f"  WARNING: figure generation failed: {exc}")
            success = False
        if success:
            print(f"  Saved {output_path}")
            created.append(filename)
        else:
            skipped.append(filename)

    print("\n=== Summary ===")
    print(f"Created ({len(created)}): {', '.join(created) if created else '(none)'}")
    print(f"Skipped ({len(skipped)}): {', '.join(skipped) if skipped else '(none)'}")


if __name__ == "__main__":
    main()
