"""Paper-ready figure generation for the TAMC workshop draft.

Goal:
    Replace the TODO figure placeholders in
    `paper_draft/tamc_workshop_draft.md` with concrete, reproducible figure
    files generated from existing experiment outputs. This script adds no
    new experiments and changes no scientific claims -- it only
    visualizes results that are already computed and saved under
    `figures/`.

Regeneration policy (since `figures/` is not tracked in git and may be
empty on a fresh clone):
    - Lightweight summaries (the three detection multi-seed CSVs, the
      runtime benchmark summary -- each a few seconds to ~70s to rerun)
      are regenerated automatically by default if missing, unless
      `--skip-expensive` is passed.
    - Moderately expensive summaries (the forecast-adaptation multi-seed
      CSVs -- a few minutes each) are only regenerated if `--regenerate-missing`
      is passed; otherwise the dependent figure is skipped with a clear
      message.
    - The topology ablation summary is never auto-regenerated by this
      script under any flag combination -- it takes ~20 minutes. If it is
      missing, the script prints the exact command to run and skips that
      figure gracefully.

Run:
    uv run python experiments/paper_figures.py
    uv run python experiments/paper_figures.py --regenerate-missing
"""

from __future__ import annotations

import subprocess
from pathlib import Path

import matplotlib.pyplot as plt
import numpy as np
import pandas as pd
from matplotlib.patches import FancyArrowPatch, FancyBboxPatch

REPO_ROOT = Path(__file__).resolve().parents[1]
FIGURES_DIR = REPO_ROOT / "figures"

DETECTION_METHODS = (
    "TAMC topological drift",
    "Autocorrelation drift",
    "Spectral drift",
    "Rolling mean drift",
    "Rolling variance drift",
)


def _run(cmd: list[str], description: str) -> bool:
    print(f"  Regenerating {description} via: {' '.join(cmd)}", flush=True)
    result = subprocess.run(cmd, cwd=REPO_ROOT, capture_output=True, text=True)
    if result.returncode != 0:
        last_line = (
            result.stderr.strip().splitlines()[-1] if result.stderr else "(no stderr)"
        )
        print(f"  WARNING: regeneration failed for {description}: {last_line}")
        return False
    return True


def ensure_csv(path: Path, cmd: list[str], description: str, allow: bool) -> bool:
    """Return True if `path` exists (regenerating it via `cmd` if missing and allowed)."""
    if path.exists():
        return True
    if not allow:
        print(
            f"  SKIP: {description} missing ({path.name}); regeneration not "
            f"enabled for this run. To create it, run:\n    {' '.join(cmd)}"
        )
        return False
    if not _run(cmd, description):
        return False
    if not path.exists():
        print(f"  WARNING: {description} still missing after regeneration attempt.")
        return False
    return True


def make_pipeline_schematic(output_path: Path) -> bool:
    """Figure 1: the closed-loop pipeline schematic. No data dependency."""
    stages = [
        "Streaming\ntime series",
        "Delay-coordinate\nembedding",
        "Sliding attractor\npoint cloud",
        "Persistent\nhomology",
        "Topological\ndrift score",
        "Gate /\nmeta-control",
        "Adapted\nforecast",
    ]

    fig, ax = plt.subplots(figsize=(12, 2.6))
    n = len(stages)
    box_width, box_height = 0.85, 0.6
    gap = 0.35
    x_positions = np.arange(n) * (box_width + gap)

    for x, label in zip(x_positions, stages):
        box = FancyBboxPatch(
            (x, 0),
            box_width,
            box_height,
            boxstyle="round,pad=0.04",
            edgecolor="black",
            facecolor="white",
        )
        ax.add_patch(box)
        ax.text(
            x + box_width / 2,
            box_height / 2,
            label,
            ha="center",
            va="center",
            fontsize=9,
        )

    for x in x_positions[:-1]:
        arrow = FancyArrowPatch(
            (x + box_width, box_height / 2),
            (x + box_width + gap, box_height / 2),
            arrowstyle="-|>",
            mutation_scale=14,
            color="black",
        )
        ax.add_patch(arrow)

    ax.set_xlim(-0.1, x_positions[-1] + box_width + 0.1)
    ax.set_ylim(-0.55, box_height + 0.15)
    ax.axis("off")
    ax.set_title(
        "TAMC closed loop: topological drift as a meta-control signal", fontsize=11
    )
    ax.text(
        x_positions[-1] / 2 + box_width / 2,
        -0.35,
        r"Forward-only: uses only $x_{\leq t}$; no labels, gradients, or model-weight updates",
        ha="center",
        va="center",
        fontsize=9,
        style="italic",
    )

    fig.tight_layout()
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return True


def make_detection_summary(output_path: Path, regenerate_lightweight: bool) -> bool:
    """Figure 2: grouped bar chart of AUROC by method, one group per system."""
    systems = [
        (
            "sine_quasiperiodic",
            FIGURES_DIR / "synthetic_regime_shift_multiseed_metrics.csv",
            [
                "uv",
                "run",
                "python",
                "experiments/synthetic_regime_shift.py",
                "--multi-seed",
                "10",
            ],
        ),
        (
            "logistic_map",
            FIGURES_DIR / "logistic_map_shift_multiseed_metrics.csv",
            [
                "uv",
                "run",
                "python",
                "experiments/logistic_map_shift.py",
                "--multi-seed",
                "10",
            ],
        ),
        (
            "lorenz",
            FIGURES_DIR / "lorenz_shift_multiseed_metrics.csv",
            [
                "uv",
                "run",
                "python",
                "experiments/lorenz_shift.py",
                "--multi-seed",
                "10",
            ],
        ),
    ]

    frames = {}
    for system, path, cmd in systems:
        if not ensure_csv(
            path, cmd, f"{system} detection multiseed metrics", regenerate_lightweight
        ):
            print(f"  SKIP: Figure 2 (detection summary) -- missing data for {system}.")
            return False
        frames[system] = pd.read_csv(path).set_index("Method")

    fig, ax = plt.subplots(figsize=(9, 5))
    n_methods = len(DETECTION_METHODS)
    n_systems = len(systems)
    bar_width = 0.8 / n_methods
    x_base = np.arange(n_systems)

    for i, method in enumerate(DETECTION_METHODS):
        means = []
        stds = []
        for system, _, _ in systems:
            frame = frames[system]
            if method in frame.index:
                means.append(frame.loc[method, "AUROC mean"])
                stds.append(frame.loc[method, "AUROC std"])
            else:
                means.append(np.nan)
                stds.append(0.0)
        offset = (i - (n_methods - 1) / 2) * bar_width
        ax.bar(
            x_base + offset, means, width=bar_width, yerr=stds, capsize=2, label=method
        )

    ax.set_xticks(x_base)
    ax.set_xticklabels([s for s, _, _ in systems])
    ax.set_ylabel("AUROC (mean +/- std, 10 seeds)")
    ax.set_ylim(0.4, 1.05)
    ax.axhline(0.5, color="gray", linestyle=":", linewidth=1)
    ax.set_title("Detection AUROC by method across controlled dynamical systems")
    ax.legend(fontsize=8, loc="lower right")

    fig.tight_layout()
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return True


def make_adaptation_tradeoff(output_path: Path, regenerate_expensive: bool) -> bool:
    """Figure 3: Net Adaptation Score, original settings + dynamical extension."""
    sine_path = FIGURES_DIR / "tamc_lite_synthetic_forecast_multiseed_metrics.csv"
    etth1_path = (
        FIGURES_DIR
        / "real_data_controlled_shift_seasonality_break_multiseed_metrics.csv"
    )
    dynamical_path = FIGURES_DIR / "dynamical_forecast_adaptation_tradeoff_summary.csv"

    ok = True
    ok &= ensure_csv(
        sine_path,
        [
            "uv",
            "run",
            "python",
            "experiments/tamc_lite_synthetic_forecast.py",
            "--multi-seed",
            "10",
        ],
        "sine forecast-adaptation multiseed metrics",
        regenerate_expensive,
    )
    ok &= ensure_csv(
        etth1_path,
        [
            "uv",
            "run",
            "python",
            "experiments/real_data_controlled_shift.py",
            "--csv-path",
            "data/ETTh1.csv",
            "--value-column",
            "OT",
            "--timestamp-column",
            "date",
            "--shift-type",
            "seasonality_break",
            "--multi-seed",
            "10",
        ],
        "ETTh1 seasonality_break multiseed metrics",
        regenerate_expensive,
    )
    ok &= ensure_csv(
        dynamical_path,
        [
            "uv",
            "run",
            "python",
            "experiments/dynamical_forecast_adaptation.py",
            "--multi-seed",
            "10",
        ],
        "dynamical forecast-adaptation tradeoff summary",
        regenerate_expensive,
    )
    if not ok:
        print("  SKIP: Figure 3 (adaptation tradeoff) -- missing required data.")
        return False

    sine_df = pd.read_csv(sine_path)
    sine_row = sine_df[
        (sine_df["Variant"] == "TAMC-gated blend")
        & (sine_df["Segment"] == "post-shift")
    ].iloc[0]

    etth1_df = pd.read_csv(etth1_path)
    etth1_row = etth1_df[
        (etth1_df["Variant"] == "TAMC-gated blend")
        & (etth1_df["Segment"] == "post-shift")
    ].iloc[0]

    dynamical_df = pd.read_csv(dynamical_path)

    fig, axes = plt.subplots(1, 2, figsize=(12, 5))

    # Panel A: original synthetic sine result + real-data ETTh1 result.
    labels_a = [
        "Sine\n(TAMC-gated blend)",
        "ETTh1 seasonality_break\n(TAMC-gated blend)",
    ]
    means_a = [
        sine_row["Net Adaptation Score mean"],
        etth1_row["Net Adaptation Score mean"],
    ]
    stds_a = [
        sine_row["Net Adaptation Score std"],
        etth1_row["Net Adaptation Score std"],
    ]
    axes[0].bar(labels_a, means_a, yerr=stds_a, capsize=4, color="tab:blue")
    axes[0].axhline(0, color="black", linewidth=1)
    axes[0].set_ylabel("Net Adaptation Score (higher is better; 0 = frozen baseline)")
    axes[0].set_title("Original synthetic + real-data result")

    # Panel B: dynamical extension, grouped by system.
    systems = ["sine_quasiperiodic", "logistic_map", "lorenz"]
    variants = [
        "TAMC-gated blend (recent-pattern)",
        "Always-on blend (recent-pattern)",
        "Adaptive rolling LinearAR forecaster",
    ]
    short_variant_labels = ["TAMC-gated", "Always-on", "Rolling LinearAR alone"]

    n_variants = len(variants)
    bar_width = 0.8 / n_variants
    x_base = np.arange(len(systems))
    for i, (variant, short_label) in enumerate(zip(variants, short_variant_labels)):
        means = []
        stds = []
        for system in systems:
            row = dynamical_df[
                (dynamical_df["System"] == system)
                & (dynamical_df["Variant"] == variant)
            ]
            if row.empty:
                means.append(np.nan)
                stds.append(0.0)
            else:
                means.append(row["Net Adaptation Score mean"].iloc[0])
                stds.append(row["Net Adaptation Score std"].iloc[0])
        offset = (i - (n_variants - 1) / 2) * bar_width
        axes[1].bar(
            x_base + offset,
            means,
            width=bar_width,
            yerr=stds,
            capsize=2,
            label=short_label,
        )

    axes[1].axhline(0, color="black", linewidth=1)
    axes[1].set_xticks(x_base)
    axes[1].set_xticklabels(systems, fontsize=8)
    axes[1].set_ylabel("Net Adaptation Score (higher is better; 0 = frozen baseline)")
    axes[1].set_title("Dynamical-system extension")
    axes[1].legend(fontsize=8)

    fig.suptitle(
        "Forecast-adaptation tradeoff: Net Adaptation Score (10-seed mean +/- std)"
    )
    fig.tight_layout()
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return True


def make_ablation_heatmap(output_path: Path) -> bool:
    """Figure 4: paper version of the topology ablation heatmap. Never auto-regenerated."""
    path = FIGURES_DIR / "topology_ablation_summary.csv"
    if not path.exists():
        print(
            "  SKIP: Figure 4 (ablation heatmap) -- "
            f"{path.name} missing. This summary takes ~20 minutes to "
            "regenerate and is never rerun automatically by this script. "
            "To create it, run:\n"
            "    uv run python experiments/topology_ablation.py --multi-seed 10"
        )
        return False

    summary_df = pd.read_csv(path)
    systems = sorted(summary_df["System"].unique())
    dimensions = sorted(summary_df["Drift Dimension"].unique())

    fig, axes = plt.subplots(
        len(dimensions),
        len(systems),
        figsize=(3.6 * len(systems), 3.2 * len(dimensions)),
        squeeze=False,
    )

    for row, dim in enumerate(dimensions):
        for col, system in enumerate(systems):
            ax = axes[row][col]
            subset = summary_df[
                (summary_df["System"] == system)
                & (summary_df["Drift Dimension"] == dim)
            ]
            pivot = subset.pivot(index="Window", columns="Delay", values="AUROC mean")
            pivot = pivot.sort_index(axis=0).sort_index(axis=1)

            image = ax.imshow(pivot.values, aspect="auto", vmin=0.5, vmax=1.0)
            ax.set_xticks(range(len(pivot.columns)))
            ax.set_xticklabels(pivot.columns, fontsize=8)
            ax.set_yticks(range(len(pivot.index)))
            ax.set_yticklabels(pivot.index, fontsize=8)
            ax.set_xlabel("Delay", fontsize=8)
            if col == 0:
                ax.set_ylabel("Window", fontsize=8)
            ax.set_title(f"{system}\nH{dim}", fontsize=9)

            for i in range(pivot.shape[0]):
                for j in range(pivot.shape[1]):
                    value = pivot.values[i, j]
                    if np.isfinite(value):
                        ax.text(
                            j, i, f"{value:.2f}", ha="center", va="center", fontsize=7
                        )

    fig.colorbar(
        image, ax=axes.ravel().tolist(), fraction=0.02, pad=0.02, label="AUROC mean"
    )
    fig.suptitle(
        "Topology ablation: AUROC mean over delay x window, by homology dimension"
    )
    fig.subplots_adjust(hspace=0.6, wspace=0.4, top=0.88)
    fig.savefig(output_path, dpi=200, bbox_inches="tight")
    plt.close(fig)
    return True


def make_runtime_summary(output_path: Path, regenerate_lightweight: bool) -> bool:
    """Figure 5: paper version of the runtime benchmark plot."""
    path = FIGURES_DIR / "runtime_benchmark_summary.csv"
    cmd = [
        "uv",
        "run",
        "python",
        "experiments/runtime_benchmark.py",
        "--seed",
        "0",
        "--n-repeat",
        "3",
        "--windows",
        "64",
        "128",
        "192",
        "--stride",
        "8",
        "--max-windows",
        "100",
    ]
    if not ensure_csv(path, cmd, "runtime benchmark summary", regenerate_lightweight):
        print("  SKIP: Figure 5 (runtime summary) -- missing data.")
        return False

    summary_df = pd.read_csv(path)
    methods = (
        "TAMC H0 drift",
        "TAMC H1 drift",
        "Spectral drift",
        "Autocorrelation drift",
        "Mean/variance drift",
    )

    fig, ax = plt.subplots(figsize=(7, 5))
    for method in methods:
        subset = summary_df[summary_df["Method"] == method].sort_values("Window")
        if subset.empty:
            continue
        ax.plot(
            subset["Window"],
            subset["Seconds Per Window mean"],
            marker="o",
            label=method,
        )

    ax.set_yscale("log")
    ax.set_xlabel("Window size")
    ax.set_ylabel("Seconds per scored window (log scale)")
    ax.set_title("Runtime cost per scored window")
    ax.legend(fontsize=8)
    fig.text(
        0.5,
        0.01,
        "Persistent homology dominates runtime; moderate windows remain practical with sparse scoring.",
        ha="center",
        fontsize=8,
        style="italic",
    )
    fig.tight_layout(rect=(0, 0.04, 1, 1))
    fig.savefig(output_path, dpi=200)
    plt.close(fig)
    return True


